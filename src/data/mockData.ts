export const generateFrameData = (serialNumber: string) => {
  const frames = []
  for (let i = 1; i <= 30; i++) {
    frames.push({
      id: i,
      timestamp: `${serialNumber}-frame-${i.toString().padStart(3, "0")}`,
      status: i <= 20 ? "labeled" : i <= 25 ? "reviewing" : "pending",
    })
  }
  return frames
}
